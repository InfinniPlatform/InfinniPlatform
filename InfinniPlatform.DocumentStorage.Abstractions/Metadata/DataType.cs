    /// Document properties data types.
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// Represents boolean type.
        /// </summary>
        Boolean,

        /// <summary>
        /// Represents int type.
        /// </summary>
        Int32,

        /// <summary>
        /// Represents long type.
        /// </summary>
        Int64,

        /// <summary>
        /// Represents double type.
        /// </summary>
        Double,

        /// <summary>
        /// Represents string type.
        /// </summary>
        String,

        /// <summary>
        /// Represents date and time type.
        /// </summary>
        DateTime,

        /// <summary>
        /// Represents timestamp type.
        /// </summary>
        Timestamp,

        /// <summary>
        /// Represents binary data type.
        /// </summary>
        Binary,

        /// <summary>
        /// Represents object type.
        /// </summary>
        Object,

        /// <summary>
        /// Represents array type.
        /// </summary>
        Array
    }
}